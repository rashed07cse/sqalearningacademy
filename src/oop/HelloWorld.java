package oop;

public class HelloWorld {

    public static void main(String[] args) {
        //Print 1 to 10 using for loop
        /*Syntax:
        for(initialization; condition; increment/decrement){
//statement or code to be executed
        }*/

        int i = 5;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
